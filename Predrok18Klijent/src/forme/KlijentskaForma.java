    private void srediFormu() {
        KlijentskiZahtev kz1 = new KlijentskiZahtev();
        kz1.setOperacija(Operacije.VRATI_VRSTE_POREZA);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz1);
        ServerskiOdgovor so1 = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        ArrayList<VrstaPoreza> vrstePoreza = (ArrayList<VrstaPoreza>) so1.getOdgovor();
        jcmbVrstaPoreza.removeAllItems();
        for (VrstaPoreza vrstaPoreza : vrstePoreza) {
            jcmbVrstaPoreza.addItem(vrstaPoreza);
        }
        
        KlijentskiZahtev kz2 = new KlijentskiZahtev();
        kz2.setOperacija(Operacije.VRATI_PORESKE_OBVEZNIKE);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz2);
        ServerskiOdgovor so2 = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        ArrayList<PoreskiObveznik> obveznici = (ArrayList<PoreskiObveznik>) so2.getOdgovor();
        jcmbPoreskiObveznik.removeAllItems();
        for (PoreskiObveznik poreskiObveznik : obveznici) {
            jcmbPoreskiObveznik.addItem(poreskiObveznik);
        }
        
        ModelTabele mt = new ModelTabele();
        jtblTabela.setModel(mt);
    }

