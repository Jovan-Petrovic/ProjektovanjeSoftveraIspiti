    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        if(jtxtGodina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja su obavezna");
            return;
        }
        ModelTabele mt = (ModelTabele) jtblTabela.getModel();
        if(mt.getLista().size()<1) {
            JOptionPane.showMessageDialog(this, "Poreska prijava mora sadrzati bar jednu stavku");
            return;
        }
        
        int godina = Integer.parseInt(jtxtGodina.getText().trim());
        PoreskiObveznik obveznik = (PoreskiObveznik) jcmbPoreskiObveznik.getSelectedItem();
        ArrayList<StavkaPoreskePrijave> stavke = mt.getLista();
        PoreskaPrijava prijava = new PoreskaPrijava(-1, godina, obveznik, inspektor, stavke);
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.SACUVAJ_PORESKU_PRIJAVU);
        kz.setParametar(prijava);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        JOptionPane.showMessageDialog(this, so.getPoruka());
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

    private void jbtnObrisiStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnObrisiStavkuActionPerformed
        int red = jtblTabela.getSelectedRow();
        ModelTabele mt = (ModelTabele) jtblTabela.getModel();
        if(red!=-1) {
            mt.obrisiStavku(red);
        } else {
            JOptionPane.showMessageDialog(this, "Morate selektovati red");
            return;
        }
    }//GEN-LAST:event_jbtnObrisiStavkuActionPerformed


