    public void srediFormu() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_STARTAPE);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        ArrayList<Kompanija> listaKompanija = (ArrayList<Kompanija>) so.getOdgovor();
        
        ModelTabeleStartapi mts = new ModelTabeleStartapi(listaKompanija);
        jtblTabela.setModel(mts);
        
        TableColumnModel tcm = jtblTabela.getColumnModel();
        TableColumn kolonaTri = tcm.getColumn(2);
        kolonaTri.setCellEditor(new DefaultCellEditor(new JCheckBox()));
    }

