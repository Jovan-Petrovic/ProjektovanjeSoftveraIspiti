    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        int selektovanRed = jtblTabela.getSelectedRow();
        
        if(selektovanRed != -1) {
            ModelTabeleStartapi mts = (ModelTabeleStartapi) jtblTabela.getModel();
            Kompanija k = mts.dajKompaniju(selektovanRed);
            double iznos = Double.parseDouble(jtxtIznos.getText().trim());
            Investicija i = new Investicija(-1, ulogovaniInvestitor, k, new Date(), iznos);
            
            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Operacije.SACUVAJ);
            kz.setParametar(i);
            KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
            ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
            
            JOptionPane.showMessageDialog(this, so.getPoruka());
        } else {
            JOptionPane.showMessageDialog(this, "Morate odabrati kompaniju");
        }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

