    private void srediTabelu() {
        ModelTabeleUtakmica mtu = new ModelTabeleUtakmica();
        jtblUtakmice.setModel(mtu);
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_UTAKMICE);
        
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        
        ArrayList<Utakmica> lista = (ArrayList<Utakmica>) so.getOdgovor();
        mtu.setLista(lista);
    }

    boolean dodajIzmenjenuUtakmicu(Utakmica u) {
        ModelTabeleUtakmica mtu = (ModelTabeleUtakmica) jtblUtakmice.getModel();
        boolean ubaceno = mtu.izmeni(u);
        return ubaceno;
    }

    
