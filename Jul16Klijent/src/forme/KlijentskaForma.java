    private void jbtnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnObrisiActionPerformed
        ModelTabeleUtakmica mtu = (ModelTabeleUtakmica) jtblUtakmice.getModel();
        int red = jtblUtakmice.getSelectedRow();
        if(red != -1) {
            mtu.obrisiUtakmicu(red);
        } else {
            JOptionPane.showMessageDialog(this, "morate selektovati utakmicu");
        }
    }//GEN-LAST:event_jbtnObrisiActionPerformed

    private void jbtnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIzmeniActionPerformed
       ModelTabeleUtakmica mtu = (ModelTabeleUtakmica) jtblUtakmice.getModel();
       int red = jtblUtakmice.getSelectedRow();
       if(red != -1) {
            Utakmica u = mtu.dajUtakmicu(red);
            IzmenaUtakmice iu = new IzmenaUtakmice(this, true);
            iu.srediUtakmicu(u);
            iu.pack();
            iu.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "morate selektovati utakmicu");
            return;
        }
//       JDialog forma = new IzmenaUtakmice(this, true);
//       forma.setVisible(true);
    }//GEN-LAST:event_jbtnIzmeniActionPerformed

    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        ModelTabeleUtakmica mtu = (ModelTabeleUtakmica) jtblUtakmice.getModel();
        ArrayList<Utakmica> listaUtakmica = mtu.vratiListuSvihUtakmica();
        
        for (Utakmica utakmica : listaUtakmica) {
            System.out.println(utakmica.getUtakmicaID()+", Status: " + utakmica.getStatus());
                    
        }
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.SACUVAJ_PROMENE);
        kz.setParametar(listaUtakmica);
        
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        
        JOptionPane.showMessageDialog(this, so.getPoruka());
        
        boolean sacuvano = (boolean) so.getOdgovor();
        if(sacuvano) {
            srediTabelu();
        }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

