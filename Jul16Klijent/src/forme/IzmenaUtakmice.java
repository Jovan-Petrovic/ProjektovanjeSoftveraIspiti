    private void jbtnZapamtiIzmeneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnZapamtiIzmeneActionPerformed
        String grupa = jtxtGrupa.getText();
        int goloviDomacin = Integer.parseInt(jtxtGoloviDomacin.getText().trim());
        int golovaGost = Integer.parseInt(jtxtGoloviGost.getText().trim());
        Reprezentacija domacin = (Reprezentacija) jcmbDomacin.getSelectedItem();
        Reprezentacija gost = (Reprezentacija) jcmbGost.getSelectedItem();
        
        Utakmica u = new Utakmica(izabranaUtakmica.getUtakmicaID(), grupa, domacin, gost, goloviDomacin, golovaGost, "izmena");
        
        boolean ubaceno = ((KlijentskaForma)this.getParent()).dodajIzmenjenuUtakmicu(u);
        
        if(ubaceno) {
            JOptionPane.showMessageDialog(this, "Uspesno sacuvane izmene");
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Neuspesno sacuvane izmene", "greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbtnZapamtiIzmeneActionPerformed

