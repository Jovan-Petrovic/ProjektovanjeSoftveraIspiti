    private void napuniComboBoxove() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_REPREZENTACIJE);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        ArrayList<Reprezentacija> lista = (ArrayList<Reprezentacija>) so.getOdgovor();
        
        jcmbDomacin.removeAllItems();
        for (Reprezentacija reprezentacija : lista) {
            jcmbDomacin.addItem(reprezentacija);
        }
        
        jcmbGost.removeAllItems();
        for (Reprezentacija reprezentacija : lista) {
            jcmbGost.addItem(reprezentacija);
        }
    }

    void srediUtakmicu(Utakmica u) {
        this.izabranaUtakmica = u;
        jtxtGrupa.setText(u.getGrupa());
        jcmbDomacin.setSelectedItem(u.getDomacin());
        System.out.println(u.getDomacin());
        jcmbGost.setSelectedItem(u.getGost());
        System.out.println(u.getGost());
        jtxtGoloviDomacin.setText((String.valueOf(u.getBrojGlovaDomacina())));
        jtxtGoloviGost.setText(String.valueOf(u.getBrojGolovaGost()));
    }
