    private void srediCombo() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_ZEMLJE);
        Komunikacija.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstanca().primiOdgovor();
        ArrayList<Zemlja> lista = (ArrayList<Zemlja>) so.getOdgovor();
        jcmbZemlja.removeAllItems();
        for (Zemlja zemlja : lista) {
            jcmbZemlja.addItem(zemlja);
        }
    }
