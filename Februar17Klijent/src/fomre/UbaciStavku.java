    private void jbtnSacuvajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajStavkuActionPerformed
        if(jtxtKupovni.getText().isEmpty() || jtxtProdajni.getText().isEmpty() || jtxtVaziZa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ne mogu prazna polja");
            return;
        }
        double kupovni = Double.parseDouble(jtxtKupovni.getText().trim());
        double prodajni = Double.parseDouble(jtxtProdajni.getText().trim());
        int vaziZa = Integer.parseInt(jtxtVaziZa.getText().trim());
        
        if(kupovni > prodajni) {
            JOptionPane.showMessageDialog(this, "Kupovni mora biti manji od prodajnog");
            return;
        }
        if(kupovni*1.05<prodajni) {
            JOptionPane.showMessageDialog(this, "Problem 5%");
            return;
        }
        if(kupovni<=0 || prodajni<=0) {
            JOptionPane.showMessageDialog(this, "Moraju biti veci od nule");
            return;
        }
        Zemlja zemlja = (Zemlja) jcmbZemlja.getSelectedItem();
        double srednji = (kupovni+prodajni)/2d;
        StavkaKursneListe stavka = new StavkaKursneListe(-1, zemlja, vaziZa, kupovni, srednji, prodajni);
        
        KlijentskaForma kf = (KlijentskaForma) getParent();
        kf.dodajStavku(stavka);
    }//GEN-LAST:event_jbtnSacuvajStavkuActionPerformed

