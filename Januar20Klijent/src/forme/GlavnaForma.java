        ArrayList<Rec> reci = new ArrayList<>();
        ModelTabele mt = (ModelTabele) jtblPrevodi.getModel();
        ArrayList<Prevod> prevodi = mt.getLista();
        for (Prevod prevod : prevodi) {
            if(!reci.contains(prevod.getRec())) {
                reci.add(prevod.getRec());               
            }
        }
        for(Rec rec : reci) {
            ArrayList<Prevod> prevodiNovi = new ArrayList<>();
            for (Prevod prevod : prevodi) {
                if(rec.equals(prevod.getRec())) {
                    prevodiNovi.add(prevod);
                }
            }
            rec.setPrevodi(prevodiNovi);
        }
        KlijentskiZahtev kz =new KlijentskiZahtev();
        kz.setOperacija(Operacije.SACUVAJ_VOL2);
        kz.setParametar(reci);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
