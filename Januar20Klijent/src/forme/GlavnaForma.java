    private void sredicombo() {
        KlijentskiZahtev kz1 = new KlijentskiZahtev();
        kz1.setOperacija(Operacije.VRATI_RECNIKE);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz1);
        ServerskiOdgovor so1 = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        ArrayList<Recnik> lista1 = (ArrayList<Recnik>) so1.getOdgovor();
        
        jcmbRecnik.removeAllItems();
        for (Recnik recnik : lista1) {
            jcmbRecnik.addItem(recnik);
        }

        
        jcmbNaZezik.removeAllItems();
        for (Recnik recnik : lista1) {
            jcmbNaZezik.addItem(recnik);
        }
    }

    private void srediTabelu() {
        ModelTabele mt = new ModelTabele();
        jtblPrevodi.setModel(mt);
    }

    

    
